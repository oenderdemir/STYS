(
    SELECT
        r.RowNo,
        r.TamKod,
        r.Kod,
        r.Ad,
        r.SeviyeNo,
        ParentTamKod = CASE
            WHEN CHARINDEX('.', r.TamKod) = 0 THEN NULL
            ELSE LEFT(r.TamKod, LEN(r.TamKod) - CHARINDEX('.', REVERSE(r.TamKod)))
        END,
        RnByTamKod = ROW_NUMBER() OVER (PARTITION BY r.TamKod ORDER BY r.RowNo)
    FROM @Rows r
),
TamKodUnique AS
(
    SELECT
        d.TamKod,
        d.Kod,
        d.Ad,
        d.SeviyeNo,
        d.ParentTamKod
    FROM DistinctRows d
    WHERE d.RnByTamKod = 1
),
SiblingNormalized AS
(
    SELECT
        t.TamKod,
        Kod = CASE
            WHEN ROW_NUMBER() OVER (PARTITION BY t.ParentTamKod, t.Kod ORDER BY t.TamKod) = 1
                THEN t.Kod
            ELSE LEFT(t.Kod, CASE WHEN LEN(t.Kod) > 13 THEN 13 ELSE LEN(t.Kod) END) + N'-'
